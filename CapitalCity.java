class CapitalCity{
	
	String capitalName;
	int capitalPopulation;
	String capitalFamousFor;
	
	CapitalCity()
	{	
	}
	
	CapitalCity(String capitalName,int capitalPopulation,String capitalFamousFor)
	{
		this.capitalName=capitalName;
		this.capitalPopulation=capitalPopulation;
		this.capitalFamousFor=capitalFamousFor;
	}
}