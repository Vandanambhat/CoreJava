//Get details of prime ministers and print it 
//name, country name, age, married , alive, duration as pm
class PrimeMinister{
	
	public static void getPrimeMinisterDetails(String name,String countryName,int age,String married,String alive,float duration)
	{
		System.out.println("Name of Prime Minister : "+name);
		System.out.println("Country Name : "+countryName);
		System.out.println("Age of the Prime Minister : "+age);
		System.out.println("Married Status of PM : "+married);
		System.out.println("Living status of PM : "+alive);
		System.out.println("Duration as PM : "+duration);
	}
}
