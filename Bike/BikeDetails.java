//Get details of bike and print 5 bike details
//company, price, color, registered number, owner name, rc no and Weight 
class BikeDetails{
	
	public static void Details(String company, int price, String color, String regNo, String name, int rcNo, int weight)
	{
		System.out.println("Bike Name is : "+company);
		System.out.println("Bike price is : "+price);
		System.out.println("Color of bike is : "+color);
		System.out.println("Registration number is : "+regNo);
		System.out.println("Owner name is : "+name);
		System.out.println("RC number is : "+rcNo);
		System.out.println("Weight of the bike is : "+weight);
	}
}